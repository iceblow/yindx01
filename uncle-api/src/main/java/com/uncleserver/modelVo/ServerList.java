package com.uncleserver.modelVo;

public class ServerList {

	private String server_name;
	private int serverid;
	private int needpic;
	private String thirdtips;
	private float deposit_price;
	public String getServer_name() {
		return server_name;
	}
	public void setServer_name(String server_name) {
		this.server_name = server_name;
	}
	public int getServerid() {
		return serverid;
	}
	public void setServerid(int serverid) {
		this.serverid = serverid;
	}
	public int getNeedpic() {
		return needpic;
	}
	public void setNeedpic(int needpic) {
		this.needpic = needpic;
	}
	public String getThirdtips() {
		return thirdtips;
	}
	public void setThirdtips(String thirdtips) {
		this.thirdtips = thirdtips;
	}
	public float getDeposit_price() {
		return deposit_price;
	}
	public void setDeposit_price(float deposit_price) {
		this.deposit_price = deposit_price;
	}
	
}
